package day02_Variablesss;

public class EscapeSequences {

    public static void main(String[] args) {
        System.out.println("Muhtar");
        System.out.println("\tMuhtar");
        System.out.println("\t\t\t\t\tMuhtar");

        // \t means a paragraph space

        System.out.println("Cybertek School");
        System.out.println("Cybertek \nSchool");
        System.out.println("My\nName\nIs\nMuhtar");
          // \ln means new line, starts with new line

        // print: My favorite TV show is "Game of Thrones"
        System.out.println("My Favorite TV show is \"Game of Thrones\"");

        // \" means double quote

        System.out.println("\\");
        //  \\ prints single slash on console
        System.out.println("/");

        System.out.println("My favorite book is \'java\'"); // \' prints the '
        System.out.println("My favorite book is 'java'");// prints '









    }
}
